var line_num = document.getElementsByClassName("line");

function print(number) {
    number = Number(number);
    for(var i = 0; i < 16; i++) { //每行
        for(var j = 0; j < 3; j++) {
            line_num[i].innerHTML= line_num[i].innerHTML+ "⚪︎";
        }
        for(var j = 0; j < 10; j++) { //每列
            if(num[number][i][j] == 0) {
                line_num[i].innerHTML= line_num[i].innerHTML+ "⚪︎";
            } else if(num[number][i][j] == 1) {
                line_num[i].innerHTML = line_num[i].innerHTML+ "<span class='light'>⚫︎</span>";
            }
        }
        for(var j = 0; j < 3; j++) {
            line_num[i].innerHTML= line_num[i].innerHTML+ "⚪︎";
        }
    }
}
function enter() {
    for(var i = 0; i < 16; i++) {
        line_num[i].innerHTML = "";
    }
    var num_all = document.getElementById("inp").value;
    const num_single = num_all.split("");
    for(i = 0; i < num_single.length; i++) {
        print(num_single[i]);
    }
}